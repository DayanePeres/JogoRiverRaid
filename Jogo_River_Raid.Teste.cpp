// INSERE NOVO JOGADOR

void Insere (TpReg Jogador[tf], int &tl, TpReg Novo)
{
    if(tl < tf)
    {
        Jogador[tl++]=Novo;
        TpReg aux;
        
        int i=tl-1;    
        
        while(i > 0 && strcmp (Jogador[i].nome, Jogador[i-1].nome) < 0)
        {
            aux=Jogador[i];
            Jogador[i]=Jogador[i-1];
            Jogador[i-1]=aux;
            i--;
        }
        
    }
}

